const { Schema, model } = require('mongoose');

const popularSchema = Schema(
  {
    idMeal: {
      type: String,
      required: true,
    },
    strMeal: {
      type: String,
      required: true,
    },
    strInstructions: {
      type: String,
      required: true,
    },
    strMealThumb: {
      type: String,
      required: true,
    },
    users: {
      type: Array,
      required: true,
    },
    date: {
      type:Array,
    }
  },
  { versionKey: false }
);

const PopularMeals = model('popularMeal', popularSchema);

module.exports = { PopularMeals };
